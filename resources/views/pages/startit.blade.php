            <h2>Kompanija sa jakom kulturom</h2>
            <p><a href="https://www.atlassian.com/" target="_blank">Atlassian</a> je kompanija koja se bavi proizvodnjom softvera namenjenog developerima, alatima poput Jire, Confluencea, Bitbucketa, Hipchata i ostalih. Svojim proizvodima su promenili i unapredili način na koji sarađuju i rade programeri širom sveta.</p>
            <p>Nekada se proizvodnja softvera optimizovala poboljšanjima kompajlera, a onda je fokus prešao na polje saradnje među developerima u istom timu. Iz ovog ugla je potekla ideja dvojice osnivača za ovu kompaniju.</p>
            <p>Tokom godina, Atlassian je porastao od malog startapa na nivo svetske kompanije koja zapošljava oko 800 ljudi i sarađuje sa 85 od 100 kompanija sa Fortune 100 liste. Kompanija je poznata po jakoj kulturi i ponosi se činjenicom da nema ni jednog zaposlenog u prodaji.</p>
            <p>Jedan od suosnivača, Skot Farkar, podelio je jednom predavanju priču o najvećim krizama koje su zadesile ovu kompaniju i tome kako</p>
            <blockquote>
                <p>…vaš startap može postati velika kompanija čak i ako usput zeznete par stvari.</p>
            </blockquote>
            <h2>Nedostatak inovacije</h2>
            <p>Kada radite na izgradnji kompanije, na početku sve izgleda haotično. Ne zna se ko šta radi, ne postoje standardi i procedure unutar organizacije.</p>
            <p>Ovaj problem rešava se postepenim oblikovanjem kompanije u nešto što liči na organizovan sistem. Problem je što često ovaj proces ubija najinovativniji deo vašeg startapa.</p>
            <blockquote>
                <p>Išlo nam je jako dobro. Imali smo velike proizvode koje su ljudi kupovali.
                    <br> Ipak, u jednom trenutku shvatio sam da, u periodu od 2004. do 2011. godine, nismo izbacili ni jedan nov proizvod. Sve što smo radili bilo je održavanje postojećeg softvera.</p>
                <p>Znao sam da je vreme za promenu. Na sledećem sastanku menadžmenta, u toku rasprave o smeru kretanja kompanije, pitao sam sve prisutne da li je neko od njih razgovarao sa korisnicima u prethodnih nekoliko nedelja.</p>
                <p>Kada se niko nije javio, predložio sam da svoje vreme bolje utrošimo u razgovoru sa našim kupcima o njihovim problemima, a ne nagađajući šta je to što njima treba.</p>
            </blockquote>
            <p>Komunikacija sa kupcima dala je Atlassianu bolji uvid u to na kojim poljima zapravo treba da inoviraju. Tako i Farkar savetuje da se po pitanju inovacija ne oslanjate samo na svoju intuiciju i razmišljanje, već da pokušate da otkrijete još jedan korisnički problem koji možete da rešite.</p>
            <p>Druga pojava koju je Skot video kao simptom većeg problema jeste inoviranje zaposlenih u pogrešnom smeru.</p>
            <blockquote>
                <p>Često bi mi prilazili zaposleni da mi pokažu svoj mali projekat na kojem rade u slobodno vreme, i za koji misle da bismo mogli da mu damo šansu kao budućem proizvodu naše kompanije.</p>
                <p>Onda bih morao da im objasnim da se mi ne bavimo iOS igrama, i drugim nasumičnim stvarima koje su mi predlagali.</p>
                <p>Zapitao sam se, zašto ljudi ne kapiraju šta mi to ovde u stvari radimo?</p>
            </blockquote>
            <p>Skot je shvatio koji se problem krije iza ovoga. Ljudi nisu imali jasnu sliku o tome šta njihova kompanija pokušava da postigne. Veliki broj njih smatrao je samo da radi u “kompaniji koja pravi softver”, i zato njihove ideje nisu bile usmerene ka zajedničkom cilju.</p>
            <p>U Atlassianu su ovo rešili definisanjem jasne vizije kompanije.</p>
            <blockquote>
                <p>Atlassian postoji da bi oslobodio potencijal svakog tima da unapredi čovečanstvo snagom softvera.</p>
            </blockquote>
            <p>Kada su imali jasnu viziju onoga što kompanija radi, ljudi su usmerili svoje inovacije ka tome. Takođe je uveden i ShipIt dan, da bi zaposleni dobili šansu da predstave svoje ideje na najbolji način.</p>
