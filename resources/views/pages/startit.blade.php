            <h2>Problemi sa skaliranjem</h2>
            <p>Skot i njegov partner znali su da, ako žele da njihova kompanija živi, ona mora da raste. Iako postoje primeri “malih džinova”, tj. kompanija koje su ostale male ali su napravile značajne rezultate na tržištu, u Atlassianu nisu našli ni jedan dobar razlog da ne ispune svoj potencijal zapošljavanjem još ljudi.</p>
            <p>Ipak, proces rasta iz startapa u korporaciju je veoma haotičan i teško je upravljati njime. Nije teško skalirati sam broj zaposlenih, teško je skalirati kulturu.</p>
            <blockquote>
                <p>Kada ste mala kompanija, kompanijske vrednosti odražavaju vrednosti osnivača…</p>
                <p>..sve dok osnivači više nisu ti koji zapošljavaju.</p>
            </blockquote>
            <p>Kada je Atlassian počeo da raste, Skot se uplašio da će u kompaniju doći ljudi kojima nije stalo do toga što oni rade. Kako biti siguran da će ljudi koji dođu u tvoju kompaniju deliti tvoje vrednosti?</p>
            <p>Za otkrivanje ključnih vrednosti kompanije, u Atlassianu su koristili takozvanu “Mars grupu”. Okupili su na jednom sastanku ljude koje bi vodili na Mars, kada bi tamo morali ponovo da izgrade kompaniju od temelja. To često nisu samo ljudi iz vrha menadžmenta, već svi oni koji najviše pokazuju u svakodnevnom ostvarivanju vizije, zaista verujući u vrednosti na kojima je kompanija zasnovana.</p>
            <blockquote>
                <p>Odlučili smo da jasno definišemo ko smo mi i šta vrednujemo. <a href="https://www.youtube.com/watch?v=tBPbWOoWYlY">Video</a> o vrednostima naše kompanije koji smo snimili postao je naš najbolji oglas za posao. Sada ljudi dolaze kod nas i kažu mi “hej, došao sam da radim jer sam čuo koje su vaše vrednosti, i delim ih”</p>
            </blockquote>
            <p>
                <a href="http://startit.rs/media/atassian_values.png"><img class="alignnone wp-image-54014" src="http://startit.rs/media/atassian_values-1024x576.png" alt="atassian_values" width="660" height="371" srcset="https://startit.rs/media/atassian_values-1024x576.png 1024w, https://startit.rs/media/atassian_values-150x84.png 150w, https://startit.rs/media/atassian_values-300x169.png 300w, https://startit.rs/media/atassian_values-570x321.png 570w" sizes="(max-width: 660px) 100vw, 660px"></a>
            </p>
            <p>Skot takođe misli da je veoma važno otpustiti ljude koji se ne uklapaju. Ako se neko ne uklapa u kulturu, treba ga pustiti da ide.</p>
            <p>Specifična situacija sa kojom se on susreo u Atlassianu bila je sa ljudima koji su neko vreme bili među najboljima, ali jednostavno nisu mogli da isprate brzinu rasta kompanije. Čovek koji je odlično vodio 30, odjednom je trebalo da vodi 300 ljudi. Nije se snašao, i, morao je da ide.</p>
            <h2>Hakerski napad</h2>
            <p>2010. godine Atlassian se suočio sa najvećom krizom u svom postojanju. U njihov sistem upao je haker, i budućnost kompanije bila je u velikoj opasnosti.</p>
            <p>Da stvari budu još gore, to se desilo nekoliko dana pre datuma koji je određen za prvu rundu investiranja.</p>
            <p>Politika svih velikih kompanija je da u ovakvim situacijama ćute i ne daju nikakve izjave za javnost. Često ni svi zaposleni ne budu upoznati sa detaljima problema.</p>
            <p>Ipak, vodeći ljudi Atlassiana odlučili su da ostanu dosledni svojim vrednostima. Svim korisnicima poslali su mejl, u kojem su otvoreno priznali da im je sistem hakovan, i da je bezbednost korisničkih podataka ugrožena.</p>
            <p>Svi zaposleni odmah su obavešteni o novonastaloj situaciji. Ljudi su podeljeni u timove. Skot i ljudi iz menadžmenta iznajmili su ceo hotel pored sedišta firme i celu kafeteriju preko puta, kako bi ljudi imali gde da ostanu. Niko nije želeo da ide kući dok se kriza ne reši.</p>
            <blockquote>
                <p>U ovakvim situacijama stara pravila više ne važe. Prišao sam jednom čoveku i rekao mu: Hej, ti sada vodiš tim od 40 ljudi, dođi za sat vremena sa rešenjem za ovaj problem.</p>
            </blockquote>
            <p>U zgradi su im bili agenti FBI-a, australijske policije i još desetak bezbednosnih i pravnih organizacija. Uz sav taj pritisak, za nekoliko dana otkrili su da je haker iz Istočne Evrope, i uspeli su da saznaju koje slabosti njihovog sistema je iskoristio za ovaj napad. Haker je odjednom jednostavno nestao, plašeći se posledica koje su ga čekale.</p>
            <p>Cela kriza je počela u petak, a već sledeće nedelje u utorak Skot je bio na sastanku sa investitorima. Svi su oni bili upućeni u problem koji se desio jer je Atlassian odlučio da bude transparentan.</p>
